# Python中默认的编码格式是 ASCII 格式，在没修改编码格式时无法正确打印汉字，所以在读取中文时会报错。

# 在Python中，变量不需要明确的声明类型来保留内存空间。当向变量分配值时，Python会自动发出声明。 等号(=)用于为变量赋值。

counter = 100          # 一个整型数
miles = 999.99       # 一个浮点数
name = "Maxsu"       # 一个字符串
site_url = "http://www.yiibai.com"  # 一个字符串

print (counter)
print (miles)
print (name)
print (site_url)


# 多重赋值Python允许同时为多个变量分配单个值。

a, b, c = 10, 20, "maxsu"
print(a)
print(b)
print(c)
